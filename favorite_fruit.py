#writing a chain of if statments.

favorite_fruits = ['grapes', 'apples', 'banana']

#this if statements check in this fruit are on the list.
if 'grapes' in favorite_fruits:
    print('You really like grapes!')
if 'organges' in favorite_fruits:
    print('\nYou really like oranges!')
if 'apples' in favorite_fruits:
    print('\nYou really like apples!')
if 'strawberry' in favorite_fruits:
    print('\nYou really like strawberries!')
if 'banana' in favorite_fruits:
    print('\nYou really like bananas!')
